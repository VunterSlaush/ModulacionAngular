    private void addListeners() 
    {
       this.pButton.addActionListener(new ActionListener() {

           @Override
           public void actionPerformed(ActionEvent e) 
           {
               String tipo = (String)pComboBox.getSelectedItem();
               int freq = (int)pFSpinner.getValue();
               int amp = (int)pASpinner.getValue();
               int phase = (int)pPSpinner.getValue();
               int desde = (int)pDSpinner.getValue();
               int hasta = (int)pHSpinner.getValue();
               boolean ruido = pCheck.isSelected();
               JFreeChart chart = GeneradorDeGraphicas.createChart(amp, freq, phase, desde, hasta,ruido, tipo);
               addPortadora(chart);
           }
       });
       
        this.mButton.addActionListener(new ActionListener() {

           @Override
           public void actionPerformed(ActionEvent e) 
           {
               String tipo = (String)mComboBox.getSelectedItem();
               int freq = (int)mFSpinner.getValue();
               int amp = (int)mASpinner.getValue();
               int phase = (int)mPSpinner.getValue();
               int desde = (int)mDSpinner.getValue();
               int hasta = (int)mHSpinner.getValue();
               boolean ruido = mCheck.isSelected();
               JFreeChart chart = GeneradorDeGraphicas.createChart(amp, freq, phase, desde, hasta,ruido, tipo);
               addModuladora(chart);
           }
       });
       
        this.jButton1.addActionListener(new ActionListener() {

           @Override
           public void actionPerformed(ActionEvent e) 
           {
               String tipo = (String)mComboBox.getSelectedItem();
               int freq = (int)mFSpinner.getValue();
               int amp = (int)mASpinner.getValue();
               int phase = (int)mPSpinner.getValue();
               int desde1 = (int)mDSpinner.getValue();
               int hasta1 = (int)mHSpinner.getValue();
               boolean ruido = mCheck.isSelected();
               double[][] m1 = GeneradorDeGraphicas.retornarMatriz(amp, freq, phase, desde1, hasta1,ruido, tipo);
               
               tipo = (String)pComboBox.getSelectedItem();
               freq = (int)pFSpinner.getValue();
               amp = (int)pASpinner.getValue();
               phase = (int)pPSpinner.getValue();
               int desde2 = (int)pDSpinner.getValue();
               int hasta2 = (int)pHSpinner.getValue();
               
               double[][] m2 = GeneradorDeGraphicas.retornarMatriz(amp, freq, phase, desde2, hasta2,ruido, tipo);
               
               
               addResultado(GeneradorDeGraphicas.pintarMatriz(GeneradorDeGraphicas.sumarMatrices(m1, m2, hasta1-desde1,hasta2-desde2 )));
           }
       });
    }

